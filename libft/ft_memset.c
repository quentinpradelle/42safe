#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t				i;
	unsigned char		*p;

	i = 0;
	p = b;
	while (len > 0)
	{
		p[i] = c;
		i++;
		len--;
	}
	return (b);
}
